#include <iostream>

using namespace std;

int main(){

    /*
    - [ ] printf with floating numbers (M_PI)
    
    %i - integer
    %f - real numbers
    %c - char values
    %s - string values


    for real numbers: %[align].[fraction]
    */

    
    printf("result = %i \n", 10);
    printf("result = %0.3f \n", 3.1456);
    printf("result = %.10f \n", 3.1456);
    
    return 0;
}