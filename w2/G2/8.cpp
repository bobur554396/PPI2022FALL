#include <iostream>
#include <cmath>

using namespace std;

int main(){
    /*
    - [ ] math functions - pow, round, ceil, floor
    */

    cout << pow(2, 3) << endl; // 2 ^ 3 = 8
    cout << round(2.3) << endl; 
    cout << ceil(2.0000001) << endl; 
    cout << floor(2.9) << endl; 

    return 0;
}